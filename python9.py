import math
x = min(5, 10 , 15)
y = max(5, 10 , 15)
print(x, y)

z = abs(-7.25) # |-7.25| = 7.25 (abs คือ ค่าสัมบูรณ์)
print(z)

v = pow(4 , 3) # 4 * 4 * 4 = 64 # (pow คือยกกำลัง)
print(v)


pi = math.pi
print(pi)

s_root = math.sqrt(64)
print(s_root)

x = math.ceil(1.3) # ปัดทศนิยม
y = math.floor(1.3) # ไม่ปัดทศนิยม
print(x)
print(y)